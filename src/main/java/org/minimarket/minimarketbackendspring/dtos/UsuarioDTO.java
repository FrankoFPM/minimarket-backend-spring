package org.minimarket.minimarketbackendspring.dtos;

import com.fasterxml.jackson.annotation.JsonIgnore;

public class UsuarioDTO {
    private String id;
    private String nombre;
    private String apellido;
    private String email;
    private String telefono;
    @JsonIgnore
    private String password;
    private String distritoId;
    private String googleId;
    private String facebookId;
    @JsonIgnore
    private String rol;
    @JsonIgnore
    private String estado;
    @JsonIgnore
    private String createdBy;
    @JsonIgnore
    private String updatedBy;

    public UsuarioDTO(String id, String nombre, String apellido, String email, String telefono, String password, String distritoId, String googleId, String facebookId, String rol, String estado, String createdBy, String updatedBy) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.email = email;
        this.telefono = telefono;
        this.password = password;
        this.distritoId = distritoId;
        this.googleId = googleId;
        this.facebookId = facebookId;
        this.rol = rol;
        this.estado = estado;
        this.createdBy = createdBy;
        this.updatedBy = updatedBy;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public String getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(String updatedBy) {
        this.updatedBy = updatedBy;
    }

    public String getGoogleId() {
        return googleId;
    }

    public void setGoogleId(String googleId) {
        this.googleId = googleId;
    }

    public String getDistritoId() {
        return distritoId;
    }

    public void setDistritoId(String distritoId) {
        this.distritoId = distritoId;
    }

    public String getFacebookId() {
        return facebookId;
    }

    public void setFacebookId(String facebookId) {
        this.facebookId = facebookId;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }
}
